package renetik.android.java.common

object CSStringConstants {
    const val NewLine = "\n"
    const val Comma = ","
    const val Semicolon = ";"
}

object CSDataConstants {
    const val KB = 1024
    const val MB = 1024 * KB
    const val GB = 1024 * MB
}

object CSComparisionConstants {
    const val Ascending = -1
    const val Equal = 0
    const val Descending = 1
}

object CSTimeConstants {
    const val QuarterSecond = 250
    const val HalfSecond = QuarterSecond * 2
    const val Second = HalfSecond * 2
    const val Minute = 60 * Second
    const val Hour = 60 * Minute
    const val Day = 24 * Hour
}